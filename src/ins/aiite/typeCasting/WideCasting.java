package ins.aiite.typeCasting;

public class WideCasting {

	public static void main(String[] args) {
		byte b = 10; 
		short s = b;
		System.out.println(s);
		 int i = s;
		 System.out.println(i);
		 long l =i; 
		 System.out.println(l);
		float f =l; 
		System.out.println(f);
		double d =f; 
		System.out.println(d);


	}

}
